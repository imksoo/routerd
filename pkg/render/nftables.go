func NftablesIPv4SourceNAT(router *api.Router) ([]byte, error) {
	aliases, err := nftOutboundAliases(router)
	if err != nil {
		return nil, err
	}

	var nats []api.Resource
	var nat44Rules []api.Resource
	var policies []api.Resource
	var policySets []api.Resource
	var zones []api.Resource
	var firewallPolicies []api.Resource
	var firewallLogs []api.Resource
	var firewallRules []api.Resource
	var mssPolicies []pathMTUPolicy
	var l2FilterVXLANS []vxlanConfig
	for _, res := range router.Spec.Resources {
		if res.Kind == "IPv4SourceNAT" {
			nats = append(nats, res)
		}
		if res.Kind == "NAT44Rule" {
			nat44Rules = append(nat44Rules, res)
		}
		if res.Kind == "IPv4PolicyRoute" {
			policies = append(policies, res)
		}
		if res.Kind == "IPv4PolicyRouteSet" {
			policySets = append(policySets, res)
		}
		if res.APIVersion == api.FirewallAPIVersion && res.Kind == "FirewallZone" {
			zones = append(zones, res)
		}
		if res.APIVersion == api.FirewallAPIVersion && res.Kind == "FirewallPolicy" {
			firewallPolicies = append(firewallPolicies, res)
		}
		if res.APIVersion == api.FirewallAPIVersion && res.Kind == "FirewallLog" {
			firewallLogs = append(firewallLogs, res)
		}
		if res.APIVersion == api.FirewallAPIVersion && res.Kind == "FirewallRule" {
			firewallRules = append(firewallRules, res)
		}
	}
	mssPolicies, err = pathMTUMSSPolicies(router)
	if err != nil {
		return nil, err
	}
	vxlans, err := vxlanConfigs(router, aliases)
	if err != nil {
		return nil, err
	}
	for _, vxlan := range vxlans {
		if vxlan.L2Filter != "none" {
			l2FilterVXLANS = append(l2FilterVXLANS, vxlan)
		}
	}
	if len(nats) == 0 && len(nat44Rules) == 0 && len(policies) == 0 && len(policySets) == 0 && len(zones) == 0 && len(firewallPolicies) == 0 && len(firewallLogs) == 0 && len(firewallRules) == 0 && len(mssPolicies) == 0 && len(l2FilterVXLANS) == 0 {
		return nil, nil
	}
	sort.Slice(nats, func(i, j int) bool { return nats[i].Metadata.Name < nats[j].Metadata.Name })
	sort.Slice(nat44Rules, func(i, j int) bool { return nat44Rules[i].Metadata.Name < nat44Rules[j].Metadata.Name })
	sort.Slice(policies, func(i, j int) bool { return policies[i].Metadata.Name < policies[j].Metadata.Name })
	sort.Slice(policySets, func(i, j int) bool { return policySets[i].Metadata.Name < policySets[j].Metadata.Name })
	sort.Slice(zones, func(i, j int) bool { return zones[i].Metadata.Name < zones[j].Metadata.Name })
	sort.Slice(firewallPolicies, func(i, j int) bool { return firewallPolicies[i].Metadata.Name < firewallPolicies[j].Metadata.Name })
	sort.Slice(firewallLogs, func(i, j int) bool { return firewallLogs[i].Metadata.Name < firewallLogs[j].Metadata.Name })
	sort.Slice(firewallRules, func(i, j int) bool { return firewallRules[i].Metadata.Name < firewallRules[j].Metadata.Name })

	var buf bytes.Buffer
	buf.WriteString("# Generated by routerd. Do not edit by hand.\n")
	if len(l2FilterVXLANS) > 0 {
		writeBridgeL2FilterTable(&buf, l2FilterVXLANS)
	}
	if len(zones) > 0 || len(firewallPolicies) > 0 || len(firewallLogs) > 0 || len(firewallRules) > 0 {
		if err := writeFirewallFilterTable(&buf, aliases, zones, firewallPolicies, firewallLogs, firewallRules, nil); err != nil {
			return nil, err
		}
	}
	if len(mssPolicies) > 0 {
		if err := writeTCPMSSClampTable(&buf, aliases, mssPolicies); err != nil {
			return nil, err
		}
	}
	if len(policies) > 0 || len(policySets) > 0 {
		if err := writeIPv4PolicyRouteTable(&buf, policies, policySets); err != nil {
			return nil, err
		}
	}
	if len(nats) > 0 || len(nat44Rules) > 0 {
		if err := writeIPv4SourceNATTable(&buf, aliases, nats, nat44Rules); err != nil {
			return nil, err
		}
	}
	return buf.Bytes(), nil
}

func NftablesIPv4PolicyRoutes(router *api.Router) ([]byte, error) {
	var policies []api.Resource
	var policySets []api.Resource
	for _, res := range router.Spec.Resources {
		if res.Kind == "IPv4PolicyRoute" {
			policies = append(policies, res)
		}
		if res.Kind == "IPv4PolicyRouteSet" {
			policySets = append(policySets, res)
		}
	}
	if len(policies) == 0 && len(policySets) == 0 {
		return nil, nil
	}
	sort.Slice(policies, func(i, j int) bool { return policies[i].Metadata.Name < policies[j].Metadata.Name })
	sort.Slice(policySets, func(i, j int) bool { return policySets[i].Metadata.Name < policySets[j].Metadata.Name })
	var buf bytes.Buffer
	if err := writeIPv4PolicyRouteTable(&buf, policies, policySets); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

func NftablesIPv4DefaultRoutePolicy(resourceID string, spec api.IPv4DefaultRoutePolicySpec, active api.IPv4DefaultRoutePolicyCandidate, healthy []api.IPv4DefaultRoutePolicyCandidate, routeSets map[string]api.IPv4PolicyRouteSetSpec) ([]byte, error) {
	matches, err := nftIPv4CIDRMatches(resourceID, spec.SourceCIDRs, spec.DestinationCIDRs, spec.ExcludeDestinationCIDRs)
	if err != nil {
		return nil, err
	}
	healthyMarks := make([]string, 0, len(healthy))
	for _, candidate := range healthy {
		if candidate.RouteSet != "" {
			routeSet, ok := routeSets[candidate.RouteSet]
			if !ok {
				return nil, fmt.Errorf("%s references missing IPv4PolicyRouteSet %q", resourceID, candidate.RouteSet)
			}
			for _, target := range routeSet.Targets {
				healthyMarks = append(healthyMarks, nftMark(target.Mark))
			}
			continue
		}
		healthyMarks = append(healthyMarks, nftMark(candidate.Mark))
	}
	sort.Strings(healthyMarks)
	var buf bytes.Buffer
	buf.WriteString("table ip routerd_default_route {\n")
	buf.WriteString("  chain prerouting {\n")
	buf.WriteString("    type filter hook prerouting priority -151; policy accept;\n")
	activeRouteSet := active.RouteSet != ""
	activeMark := nftMark(active.Mark)
	for _, match := range matches {
		prefix := strings.TrimSpace(match)
		if prefix != "" {
			prefix += " "
		}
		if len(healthyMarks) > 0 {
			set := "{ " + strings.Join(healthyMarks, ", ") + " }"
			buf.WriteString("    " + prefix + "ct mark " + set + " meta mark set ct mark\n")
			if activeRouteSet {
				buf.WriteString("    " + prefix + "ct mark != 0x0 ct mark != " + set + " meta mark set 0x0 ct mark set meta mark\n")
			} else {
				buf.WriteString("    " + prefix + "ct mark != 0x0 ct mark != " + set + " meta mark set " + activeMark + " ct mark set meta mark\n")
			}
		}
		if !activeRouteSet {
			buf.WriteString("    " + prefix + "ct mark 0x0 meta mark set " + activeMark + " ct mark set meta mark\n")
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
	return buf.Bytes(), nil
}

type NAT44RenderRule struct {
	Name                    string
	Type                    string
	EgressInterface         string
	SourceRanges            []string
	DestinationCIDRs        []string
	ExcludeDestinationCIDRs []string
	SNATAddress             string
}

func NftablesNAT44Rules(rules []NAT44RenderRule) ([]byte, error) {
	if len(rules) == 0 {
		return nil, nil
	}
	rules = append([]NAT44RenderRule(nil), rules...)
	sort.Slice(rules, func(i, j int) bool { return rules[i].Name < rules[j].Name })
	var buf bytes.Buffer
	buf.WriteString("# Generated by routerd. Do not edit by hand.\n")
	buf.WriteString("table ip routerd_nat {\n")
	buf.WriteString("  chain postrouting {\n")
	buf.WriteString("    type nat hook postrouting priority srcnat; policy accept;\n")
	for _, rule := range rules {
		if err := writeNAT44RenderRule(&buf, rule); err != nil {
			return nil, err
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
	return buf.Bytes(), nil
}

func nftOutboundAliases(router *api.Router) (map[string]string, error) {
	aliases := map[string]string{}
	for _, res := range router.Spec.Resources {
		switch res.Kind {
		case "Interface":
			spec, err := res.InterfaceSpec()
			if err != nil {
				return nil, err
			}
			aliases[res.Metadata.Name] = spec.IfName
		case "PPPoEInterface":
			spec, err := res.PPPoEInterfaceSpec()
			if err != nil {
				return nil, err
			}
			aliases[res.Metadata.Name] = defaultString(spec.IfName, "ppp-"+res.Metadata.Name)
		case "DSLiteTunnel":
			spec, err := res.DSLiteTunnelSpec()
			if err != nil {
				return nil, err
			}
			aliases[res.Metadata.Name] = defaultString(spec.TunnelName, res.Metadata.Name)
		case "Bridge":
			spec, err := res.BridgeSpec()
			if err != nil {
				return nil, err
			}
			aliases[res.Metadata.Name] = defaultString(spec.IfName, res.Metadata.Name)
		case "VXLANSegment":
			spec, err := res.VXLANSegmentSpec()
			if err != nil {
				return nil, err
			}
			aliases[res.Metadata.Name] = defaultString(spec.IfName, res.Metadata.Name)
		}
	}
	return aliases, nil
}

type vxlanUnderlayPort struct {
	IfName  string
	UDPPort int
}

func vxlanUnderlayPorts(vxlans []vxlanConfig) []vxlanUnderlayPort {
	seen := map[string]bool{}
	var out []vxlanUnderlayPort
	for _, v := range vxlans {
		if v.UnderlayIfName == "" {
			continue
		}
		key := v.UnderlayIfName + ":" + strconv.Itoa(v.UDPPort)
		if seen[key] {
			continue
		}
		seen[key] = true
		out = append(out, vxlanUnderlayPort{IfName: v.UnderlayIfName, UDPPort: v.UDPPort})
	}
	sort.Slice(out, func(i, j int) bool {
		if out[i].IfName != out[j].IfName {
			return out[i].IfName < out[j].IfName
		}
		return out[i].UDPPort < out[j].UDPPort
	})
	return out
}

func writeBridgeL2FilterTable(buf *bytes.Buffer, vxlans []vxlanConfig) {
	sort.Slice(vxlans, func(i, j int) bool { return vxlans[i].IfName < vxlans[j].IfName })
	buf.WriteString("table bridge routerd_l2_filter {\n")
	buf.WriteString("  chain forward {\n")
	buf.WriteString("    type filter hook forward priority filter; policy accept;\n")
	for _, vxlan := range vxlans {
		ifname := nftQuote(vxlan.IfName)
		for _, direction := range []string{"iifname", "oifname"} {
			buf.WriteString("    " + direction + " " + ifname + " ether type ip udp sport { 67, 68 } counter drop\n")
			buf.WriteString("    " + direction + " " + ifname + " ether type ip udp dport { 67, 68 } counter drop\n")
			buf.WriteString("    " + direction + " " + ifname + " ether type ip6 udp sport { 546, 547 } counter drop\n")
			buf.WriteString("    " + direction + " " + ifname + " ether type ip6 udp dport { 546, 547 } counter drop\n")
			buf.WriteString("    " + direction + " " + ifname + " ether type ip6 icmpv6 type { nd-router-solicit, nd-router-advert, nd-neighbor-solicit, nd-neighbor-advert } counter drop\n")
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
}

type FirewallHole struct {
	Name      string
	FromZone  string
	ToZone    string
	Protocol  string
	Port      int
	Action    string
	Direction string
	Comment   string
}

func NftablesFirewall(router *api.Router, holes []FirewallHole) ([]byte, error) {
	aliases, err := nftOutboundAliases(router)
	if err != nil {
		return nil, err
	}
	var zones []api.Resource
	var policies []api.Resource
	var logs []api.Resource
	var rules []api.Resource
	for _, res := range router.Spec.Resources {
		if res.APIVersion != api.FirewallAPIVersion {
			continue
		}
		switch res.Kind {
		case "FirewallZone":
			zones = append(zones, res)
		case "FirewallPolicy":
			policies = append(policies, res)
		case "FirewallLog":
			logs = append(logs, res)
		case "FirewallRule":
			rules = append(rules, res)
		}
	}
	if len(zones) == 0 && len(rules) == 0 && len(holes) == 0 {
		return nil, nil
	}
	sort.Slice(zones, func(i, j int) bool { return zones[i].Metadata.Name < zones[j].Metadata.Name })
	sort.Slice(policies, func(i, j int) bool { return policies[i].Metadata.Name < policies[j].Metadata.Name })
	sort.Slice(logs, func(i, j int) bool { return logs[i].Metadata.Name < logs[j].Metadata.Name })
	sort.Slice(rules, func(i, j int) bool { return rules[i].Metadata.Name < rules[j].Metadata.Name })
	var buf bytes.Buffer
	buf.WriteString("# Generated by routerd. Do not edit by hand.\n")
	if err := writeFirewallFilterTable(&buf, aliases, zones, policies, logs, rules, holes); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

func writeFirewallFilterTable(buf *bytes.Buffer, aliases map[string]string, zones []api.Resource, policies []api.Resource, logs []api.Resource, rules []api.Resource, holes []FirewallHole) error {
	zoneMap, err := nftFirewallZones(aliases, zones)
	if err != nil {
		return err
	}
	policy := firewallPolicyOptions(policies)
	logging := firewallLogOptions(logs)
	buf.WriteString("table inet routerd_filter {\n")
	for _, zone := range sortedFirewallZones(zoneMap) {
		if len(zone.IfNames) > 0 {
			buf.WriteString("  set " + nftSetName("if_"+zone.Name) + " { type ifname; elements = { " + nftQuotedList(zone.IfNames) + " } }\n")
		}
	}
	buf.WriteString("  chain input {\n")
	buf.WriteString("    type filter hook input priority filter; policy drop;\n")
	buf.WriteString("    ct state invalid counter drop\n")
	buf.WriteString("    ct state { established, related } counter accept\n")
	buf.WriteString("    iifname \"lo\" counter accept\n")
	buf.WriteString("    meta l4proto ipv6-icmp counter accept\n")
	for _, zone := range sortedFirewallZones(zoneMap) {
		if len(zone.IfNames) == 0 {
			continue
		}
		buf.WriteString("    iifname @if_" + nftIdent(zone.Name) + " jump " + nftChainName(zone.Name+"-to-self") + "\n")
	}
	if policy.LogDeny {
		buf.WriteString("    counter " + nftLogExpr("routerd firewall input deny ", logging) + " drop\n")
	} else {
		buf.WriteString("    counter drop\n")
	}
	buf.WriteString("  }\n")
	buf.WriteString("  chain forward {\n")
	buf.WriteString("    type filter hook forward priority filter; policy drop;\n")
	buf.WriteString("    ct state invalid counter drop\n")
	buf.WriteString("    ct state { established, related } counter accept\n")
	buf.WriteString("    meta l4proto ipv6-icmp counter accept\n")
	for _, from := range sortedFirewallZones(zoneMap) {
		for _, to := range sortedFirewallZones(zoneMap) {
			if len(from.IfNames) == 0 || len(to.IfNames) == 0 {
				continue
			}
			buf.WriteString("    iifname @if_" + nftIdent(from.Name) + " oifname @if_" + nftIdent(to.Name) + " jump " + nftChainName(from.Name+"-to-"+to.Name) + "\n")
		}
	}
	if policy.LogDeny {
		buf.WriteString("    counter " + nftLogExpr("routerd firewall forward deny ", logging) + " drop\n")
	} else {
		buf.WriteString("    counter drop\n")
	}
	buf.WriteString("  }\n")
	for _, from := range sortedFirewallZones(zoneMap) {
		if err := writeFirewallPairChain(buf, from, firewallZone{Name: "self", Role: "self"}, rules, holes, policy, logging); err != nil {
			return err
		}
		for _, to := range sortedFirewallZones(zoneMap) {
			if err := writeFirewallPairChain(buf, from, to, rules, holes, policy, logging); err != nil {
				return err
			}
		}
	}
	buf.WriteString("}\n")
	return nil
}

type firewallPolicy struct {
	LogDeny        bool
	SameRoleAccept bool
}

type firewallLogging struct {
	Enabled bool
	Group   int
}

type firewallZone struct {
	Name    string
	Role    string
	IfNames []string
}

func firewallPolicyOptions(resources []api.Resource) firewallPolicy {
	out := firewallPolicy{SameRoleAccept: true}
	for _, res := range resources {
		spec, err := res.FirewallPolicySpec()
		if err != nil {
			continue
		}
		out.LogDeny = out.LogDeny || spec.LogDeny
		if spec.SameRoleAccept {
			out.SameRoleAccept = true
		}
	}
	return out
}

func firewallLogOptions(resources []api.Resource) firewallLogging {
	for _, res := range resources {
		spec, err := res.FirewallLogSpec()
		if err != nil || !spec.Enabled {
			continue
		}
		group := spec.NFLogGroup
		if group == 0 {
			group = 1
		}
		return firewallLogging{Enabled: true, Group: group}
	}
	return firewallLogging{}
}

func nftLogExpr(prefix string, logging firewallLogging) string {
	expr := "log prefix " + nftQuote(prefix)
	if logging.Enabled {
		expr += " group " + strconv.Itoa(logging.Group)
	}
	return expr
}

func writeFirewallPairChain(buf *bytes.Buffer, from firewallZone, to firewallZone, rules []api.Resource, holes []FirewallHole, policy firewallPolicy, logging firewallLogging) error {
	chain := nftChainName(from.Name + "-to-" + to.Name)
	buf.WriteString("  chain " + chain + " {\n")
	for _, hole := range holes {
		if hole.FromZone == from.Name && hole.ToZone == to.Name {
			buf.WriteString("    " + nftFirewallHoleExpr(hole) + "\n")
		}
	}
	for _, res := range rules {
		spec, err := res.FirewallRuleSpec()
		if err != nil {
			return err
		}
		if spec.FromZone != from.Name || spec.ToZone != to.Name {
			continue
		}
		expr, err := nftFirewallRuleExpr(res.Metadata.Name, spec, logging)
		if err != nil {
			return err
		}
		buf.WriteString("    " + expr + "\n")
	}
	action := implicitFirewallAction(from.Role, to.Role, policy)
	if policy.LogDeny && (action == "drop" || action == "reject") {
		buf.WriteString("    counter " + nftLogExpr("routerd firewall "+from.Name+"-to-"+to.Name+" deny ", logging) + " " + action + "\n")
	} else {
		buf.WriteString("    counter " + action + "\n")
	}
	buf.WriteString("  }\n")
	return nil
}

func implicitFirewallAction(fromRole, toRole string, policy firewallPolicy) string {
	if toRole == "self" {
		switch fromRole {
		case "mgmt", "trust":
			return "accept"
		default:
			return "drop"
		}
	}
	if policy.SameRoleAccept && fromRole == toRole {
		return "accept"
	}
	switch fromRole {
	case "mgmt":
		return "accept"
	case "trust":
		if toRole == "mgmt" {
			return "drop"
		}
		return "accept"
	default:
		return "drop"
	}
}

func nftFirewallRuleExpr(name string, spec api.FirewallRuleSpec, logging firewallLogging) (string, error) {
	var parts []string
	if len(spec.SourceCIDRs) > 0 {
		match, err := nftFirewallCIDRMatch(name, spec.SourceCIDRs)
		if err != nil {
			return "", err
		}
		parts = append(parts, match)
	}
	if proto := nftFirewallProtocol(spec.Protocol); proto != "" {
		parts = append(parts, proto)
	}
	if spec.Port != 0 {
		parts = append(parts, "dport "+strconv.Itoa(spec.Port))
	}
	if spec.Log {
		parts = append(parts, nftLogExpr("routerd firewall "+name+" ", logging))
	}
	parts = append(parts, "counter", spec.Action)
	return strings.Join(parts, " "), nil
}

func nftFirewallHoleExpr(hole FirewallHole) string {
	var parts []string
	if proto := nftFirewallProtocol(hole.Protocol); proto != "" {
		parts = append(parts, proto)
	}
	if hole.Port != 0 {
		parts = append(parts, "dport "+strconv.Itoa(hole.Port))
	}
	parts = append(parts, "counter", defaultString(hole.Action, "accept"))
	if hole.Comment != "" {
		parts = append(parts, "comment "+nftQuote(hole.Comment))
	}
	return strings.Join(parts, " ")
}

func nftFirewallProtocol(protocol string) string {
	switch protocol {
	case "tcp", "udp":
		return protocol
	case "icmp":
		return "ip protocol icmp"
	case "icmpv6", "ipv6-icmp":
		return "meta l4proto ipv6-icmp"
	case "ipip":
		return "meta nfproto ipv6 meta l4proto 4"
	default:
		return ""
	}
}

func nftFirewallCIDRMatch(resourceID string, cidrs []string) (string, error) {
	var v4, v6 []string
	for _, cidr := range cidrs {
		prefix, err := netip.ParsePrefix(cidr)
		if err != nil {
			return "", fmt.Errorf("%s has invalid source CIDR %q", resourceID, cidr)
		}
		if prefix.Addr().Is4() {
			v4 = append(v4, prefix.Masked().String())
		} else {
			v6 = append(v6, prefix.Masked().String())
		}
	}
	var out []string
	if len(v4) > 0 {
		sort.Strings(v4)
		if len(v4) == 1 {
			out = append(out, "ip saddr "+v4[0])
		} else {
			out = append(out, "ip saddr { "+strings.Join(v4, ", ")+" }")
		}
	}
	if len(v6) > 0 {
		sort.Strings(v6)
		if len(v6) == 1 {
			out = append(out, "ip6 saddr "+v6[0])
		} else {
			out = append(out, "ip6 saddr { "+strings.Join(v6, ", ")+" }")
		}
	}
	return strings.Join(out, " "), nil
}

func nftFirewallZones(aliases map[string]string, zones []api.Resource) (map[string]firewallZone, error) {
	result := map[string]firewallZone{}
	for _, res := range zones {
		spec, err := res.FirewallZoneSpec()
		if err != nil {
			return nil, err
		}
		var ifnames []string
		for _, iface := range spec.Interfaces {
			ref := iface
			if _, name, ok := strings.Cut(iface, "/"); ok {
				ref = name
			}
			ifname := aliases[ref]
			if ifname == "" {
				return nil, fmt.Errorf("%s references interface with empty ifname %q", res.ID(), iface)
			}
			ifnames = append(ifnames, ifname)
		}
		sort.Strings(ifnames)
		result[res.Metadata.Name] = firewallZone{Name: res.Metadata.Name, Role: spec.Role, IfNames: compactStrings(ifnames)}
	}
	return result, nil
}

func sortedFirewallZones(zones map[string]firewallZone) []firewallZone {
	out := make([]firewallZone, 0, len(zones))
	for _, zone := range zones {
		out = append(out, zone)
	}
	sort.Slice(out, func(i, j int) bool { return out[i].Name < out[j].Name })
	return out
}

func nftIdent(name string) string {
	replacer := strings.NewReplacer("-", "_", ".", "_", "/", "_")
	return replacer.Replace(name)
}

