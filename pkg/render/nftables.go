func nftFirewallRateLimitExpr(limit api.FirewallRateLimitSpec) string {
	rate := limit.Rate
	unit := strings.TrimSpace(limit.Unit)
	per := strings.TrimSpace(limit.Per)
	if rate == 0 && limit.PacketsPerSecond > 0 {
		rate = limit.PacketsPerSecond
	}
	if unit == "" && rate > 0 {
		unit = "packet"
	}
	if per == "" && rate > 0 {
		per = "second"
	}
	if rate <= 0 {
		return ""
	}
	value := strconv.Itoa(rate)
	burstUnit := "packets"
	switch unit {
	case "byte":
		value += " bytes"
		burstUnit = "bytes"
	case "kilobyte":
		value += " kbytes"
		burstUnit = "kbytes"
	case "megabyte":
		value += " mbytes"
		burstUnit = "mbytes"
	}
	expr := "limit rate over " + value + "/" + per
	if limit.Burst > 0 {
		expr += " burst " + strconv.Itoa(limit.Burst) + " " + burstUnit
	}
	return expr
}

func nftFirewallConnLimitExprs(name string, limit api.FirewallConnLimitSpec, family string, logging firewallLogging) []string {
	if limit.MaxPerSource <= 0 {
		return []string{""}
	}
	safeName := nftSetName("routerd_conn_" + name)
	logExpr := ""
	if limit.Log {
		logExpr = nftLogExpr("routerd firewall "+name+" conn-limit ", logging)
	}
	if family == "ip" {
		return []string{strings.TrimSpace("meter " + safeName + "_v4 { ip saddr ct count over " + strconv.Itoa(limit.MaxPerSource) + " } " + logExpr)}
	}
	if family == "ip6" {
		return []string{strings.TrimSpace("meter " + safeName + "_v6 { ip6 saddr ct count over " + strconv.Itoa(limit.MaxPerSource) + " } " + logExpr)}
	}
	return []string{
		strings.TrimSpace("meta nfproto ipv4 meter " + safeName + "_v4 { ip saddr ct count over " + strconv.Itoa(limit.MaxPerSource) + " } " + logExpr),
		strings.TrimSpace("meta nfproto ipv6 meter " + safeName + "_v6 { ip6 saddr ct count over " + strconv.Itoa(limit.MaxPerSource) + " } " + logExpr),
	}
}

func nftFirewallCIDRMatch(resourceID, direction string, cidrs []string) (string, error) {
	var v4, v6 []string
	for _, cidr := range cidrs {
		prefix, err := netip.ParsePrefix(cidr)
		if err != nil {
			return "", fmt.Errorf("%s has invalid %s CIDR %q", resourceID, direction, cidr)
		}
		if prefix.Addr().Is4() {
			v4 = append(v4, prefix.Masked().String())
		} else {
			v6 = append(v6, prefix.Masked().String())
		}
	}
	var out []string
	if len(v4) > 0 {
		sort.Strings(v4)
		if len(v4) == 1 {
			out = append(out, "ip "+direction+" "+v4[0])
		} else {
			out = append(out, "ip "+direction+" { "+strings.Join(v4, ", ")+" }")
		}
	}
	if len(v6) > 0 {
		sort.Strings(v6)
		if len(v6) == 1 {
			out = append(out, "ip6 "+direction+" "+v6[0])
		} else {
			out = append(out, "ip6 "+direction+" { "+strings.Join(v6, ", ")+" }")
		}
	}
	return strings.Join(out, " "), nil
}

type nftFirewallMatch struct {
	Family string
	Expr   string
}

func nftFirewallMatches(resourceID string, spec api.FirewallRuleSpec, addressSets map[string]nftIPAddressSet) ([]string, error) {
	matches, err := nftFirewallMatchAlternatives(resourceID, spec, addressSets)
	if err != nil {
		return nil, err
	}
	out := make([]string, 0, len(matches))
	for _, match := range matches {
		out = append(out, match.Expr)
	}
	return out, nil
}

func nftFirewallMatchAlternatives(resourceID string, spec api.FirewallRuleSpec, addressSets map[string]nftIPAddressSet) ([]nftFirewallMatch, error) {
	sources, err := nftFirewallCIDRMatchAlternatives(resourceID, "saddr", spec.SourceCIDRs)
	if err != nil {
		return nil, err
	}
	destinations, err := nftFirewallDestinationAlternatives(resourceID, spec, addressSets)
	if err != nil {
		return nil, err
	}
	excludes, err := nftFirewallExcludedDestinationMatches(resourceID, spec.ExcludeDestinationSetRefs, addressSets)
	if err != nil {
		return nil, err
	}
	if len(sources) == 0 {
		sources = []nftFirewallMatch{{}}
	}
	if len(destinations) == 0 {
		destinations = []nftFirewallMatch{{}}
	}
	var out []nftFirewallMatch
	for _, source := range sources {
		for _, destination := range destinations {
			family, ok := mergeNftMatchFamilies(source.Family, destination.Family)
			if !ok {
				continue
			}
			if protoFamily := nftFirewallProtocolFamily(spec.Protocol); protoFamily != "" {
				var protoOK bool
				family, protoOK = mergeNftMatchFamilies(family, protoFamily)
				if !protoOK {
					continue
				}
			}
			parts := []string{source.Expr, destination.Expr}
			for _, exclude := range excludes {
				if exclude.Family == "" || family == "" || exclude.Family == family {
					parts = append(parts, exclude.Expr)
				}
			}
			out = append(out, nftFirewallMatch{Family: family, Expr: strings.Join(strings.Fields(strings.Join(parts, " ")), " ")})
		}
	}
	if len(out) == 0 {
		return nil, fmt.Errorf("%s has no compatible firewall match alternatives", resourceID)
	}
	return out, nil
}

func nftFirewallCIDRMatchAlternatives(resourceID, direction string, cidrs []string) ([]nftFirewallMatch, error) {
	var out []nftFirewallMatch
	for _, cidr := range cidrs {
		prefix, err := netip.ParsePrefix(cidr)
		if err != nil {
			return nil, fmt.Errorf("%s has invalid %s CIDR %q", resourceID, direction, cidr)
		}
		prefix = prefix.Masked()
		if prefix.Addr().Is4() {
			out = append(out, nftFirewallMatch{Family: "ip", Expr: "ip " + direction + " " + prefix.String()})
		} else {
			out = append(out, nftFirewallMatch{Family: "ip6", Expr: "ip6 " + direction + " " + prefix.String()})
		}
	}
	return out, nil
}

func nftFirewallDestinationAlternatives(resourceID string, spec api.FirewallRuleSpec, addressSets map[string]nftIPAddressSet) ([]nftFirewallMatch, error) {
	out, err := nftFirewallCIDRMatchAlternatives(resourceID, "daddr", spec.DestinationCIDRs)
	if err != nil {
		return nil, err
	}
	for _, ref := range spec.DestinationSetRefs {
		set, err := nftIPAddressSetRef(resourceID, ref, addressSets)
		if err != nil {
			return nil, err
		}
		if set.IPv4Enabled {
			out = append(out, nftFirewallMatch{Family: "ip", Expr: "ip daddr @" + NftFirewallIPAddressSetName(set.Name, "ip")})
		}
		if set.IPv6Enabled {
			out = append(out, nftFirewallMatch{Family: "ip6", Expr: "ip6 daddr @" + NftFirewallIPAddressSetName(set.Name, "ip6")})
		}
	}
	return out, nil
}

func nftFirewallExcludedDestinationMatches(resourceID string, refs []string, addressSets map[string]nftIPAddressSet) ([]nftFirewallMatch, error) {
	var out []nftFirewallMatch
	for _, ref := range refs {
		set, err := nftIPAddressSetRef(resourceID, ref, addressSets)
		if err != nil {
			return nil, err
		}
		if set.IPv4Enabled {
			out = append(out, nftFirewallMatch{Family: "ip", Expr: "ip daddr != @" + NftFirewallIPAddressSetName(set.Name, "ip")})
		}
		if set.IPv6Enabled {
			out = append(out, nftFirewallMatch{Family: "ip6", Expr: "ip6 daddr != @" + NftFirewallIPAddressSetName(set.Name, "ip6")})
		}
	}
	return out, nil
}

func mergeNftMatchFamilies(a, b string) (string, bool) {
	if a == "" {
		return b, true
	}
	if b == "" || a == b {
		return a, true
	}
	return "", false
}

func nftFirewallZones(aliases map[string]string, zones []api.Resource) (map[string]firewallZone, error) {
	result := map[string]firewallZone{}
	for _, res := range zones {
		spec, err := res.FirewallZoneSpec()
		if err != nil {
			return nil, err
		}
		var ifnames []string
		for _, iface := range spec.Interfaces {
			ref := iface
			if _, name, ok := strings.Cut(iface, "/"); ok {
				ref = name
			}
			ifname := aliases[ref]
			if ifname == "" {
				return nil, fmt.Errorf("%s references interface with empty ifname %q", res.ID(), iface)
			}
			ifnames = append(ifnames, ifname)
		}
		sort.Strings(ifnames)
		result[res.Metadata.Name] = firewallZone{Name: res.Metadata.Name, Role: spec.Role, IfNames: compactStrings(ifnames)}
	}
	return result, nil
}

func sortedFirewallZones(zones map[string]firewallZone) []firewallZone {
	out := make([]firewallZone, 0, len(zones))
	for _, zone := range zones {
		out = append(out, zone)
	}
	sort.Slice(out, func(i, j int) bool { return out[i].Name < out[j].Name })
	return out
}

