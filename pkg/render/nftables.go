func nftIPv4SourcesMatch(resourceID string, sources []string) (string, error) {
	if len(sources) == 0 {
		return "", nil
	}
	var values []string
	for _, source := range sources {
		prefix, err := netip.ParsePrefix(source)
		if err != nil || !prefix.Addr().Is4() {
			return "", fmt.Errorf("%s has invalid IPv4 source CIDR %q", resourceID, source)
		}
		values = append(values, prefix.Masked().String())
	}
	sort.Strings(values)
	if len(values) == 1 {
		return " ip saddr " + values[0], nil
	}
	return " ip saddr { " + strings.Join(values, ", ") + " }", nil
}

func writeNAT44Table(buf *bytes.Buffer, router *api.Router, aliases map[string]string, nat44Rules []api.Resource, ingressRules []ingressNATRule, redirectRules []localServiceRedirectRule, addressSets []nftIPAddressSet, addressSetMap map[string]nftIPAddressSet) error {
	writeNftTablePreamble(buf, "ip", "routerd_nat")
	buf.WriteString("table ip routerd_nat {\n")
	writeIPv4AddressSets(buf, addressSets)
	if len(ingressRules) > 0 || hasIPv4LocalServiceRedirectRules(redirectRules) {
		writeIngressDNATChains(buf, ingressRules)
		buf.WriteString("  chain prerouting {\n")
		buf.WriteString("    type nat hook prerouting priority dstnat; policy accept;\n")
		writeLocalServiceRedirectRules(buf, redirectRules, "ip")
		for _, rule := range ingressRules {
			writeIngressDNATRule(buf, rule, rule.ListenInterface, "")
			for _, ifname := range rule.HairpinInterfaces {
				if ifname == rule.ListenInterface {
					continue
				}
				writeIngressDNATRule(buf, rule, ifname, " hairpin")
			}
		}
		buf.WriteString("  }\n")
	}
	buf.WriteString("  chain postrouting {\n")
	buf.WriteString("    type nat hook postrouting priority srcnat; policy accept;\n")
	writeIngressHairpinSNATRules(buf, ingressRules)
	for _, res := range nat44Rules {
		spec, err := res.NAT44RuleSpec()
		if err != nil {
			return err
		}
		if spec.OutboundInterface != "" || len(spec.SourceCIDRs) > 0 || spec.Translation.Type != "" {
			ifname := aliases[spec.OutboundInterface]
			if ifname == "" {
				return fmt.Errorf("%s references outbound interface with empty ifname", res.ID())
			}
			for _, cidr := range spec.SourceCIDRs {
				prefix, err := netip.ParsePrefix(cidr)
				if err != nil || !prefix.Addr().Is4() {
					return fmt.Errorf("%s has invalid IPv4 source CIDR %q", res.ID(), cidr)
				}
				rules, err := nftSourceNATRules(ifname, prefix.Masked().String(), spec.Translation)
				if err != nil {
					return fmt.Errorf("%s: %w", res.ID(), err)
				}
				for _, rule := range rules {
					buf.WriteString("    " + rule + "\n")
				}
			}
			continue
		}
		if spec.EgressInterface == "" {
			if spec.EgressPolicyRef != "" {
				buf.WriteString("    # " + res.ID() + " skipped: egressInterface is resolved by " + spec.EgressPolicyRef + " at runtime\n")
				continue
			}
			return fmt.Errorf("%s needs resolved egressInterface for static nftables render", res.ID())
		}
		ifname := aliases[spec.EgressInterface]
		if ifname == "" {
			return fmt.Errorf("%s references egress interface with empty ifname", res.ID())
		}
		snatAddress := spec.SNATAddress
		if snatAddress == "" && spec.SNATAddressFrom.Resource != "" {
			var err error
			snatAddress, err = renderAddressFromResource(router, spec.SNATAddressFrom)
			if err != nil {
				return fmt.Errorf("%s spec.snatAddressFrom: %w", res.ID(), err)
			}
		}
		if err := writeNAT44RenderRule(buf, NAT44RenderRule{
			Name:                      res.Metadata.Name,
			Type:                      spec.Type,
			EgressInterface:           ifname,
			SourceRanges:              spec.SourceRanges,
			DestinationCIDRs:          spec.DestinationCIDRs,
			DestinationSetRefs:        spec.DestinationSetRefs,
			ExcludeDestinationCIDRs:   spec.ExcludeDestinationCIDRs,
			ExcludeDestinationSetRefs: spec.ExcludeDestinationSetRefs,
			SNATAddress:               snatAddress,
		}, addressSetMap); err != nil {
			return err
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
	return nil
}

func writeIngressDNATChains(buf *bytes.Buffer, rules []ingressNATRule) {
	for _, rule := range rules {
		if !ingressUsesDistribution(rule) {
			continue
		}
		for i, target := range rule.Targets {
			buf.WriteString("  chain " + ingressDNATChainName(rule, i) + " {\n")
			buf.WriteString("    " + rule.Protocol + " dport " + strconv.Itoa(rule.ListenPort) + " counter dnat to " + target.Address + ":" + strconv.Itoa(target.Port) + " comment " + nftQuote("routerd "+rule.ResourceKind+" "+rule.Name+" "+target.Name) + "\n")
			buf.WriteString("  }\n")
		}
	}
}

func writeIngressDNATRule(buf *bytes.Buffer, rule ingressNATRule, ifname string, commentSuffix string) {
	var parts []string
	parts = append(parts, "iifname "+nftQuote(ifname))
	if rule.ListenAddress != "" {
		parts = append(parts, "ip daddr "+rule.ListenAddress)
	}
	parts = append(parts, rule.Protocol)
	parts = append(parts, "dport "+strconv.Itoa(rule.ListenPort))
	if ingressUsesDistribution(rule) {
		parts = append(parts, ingressSelectionExpression(rule)+" vmap { "+ingressDNATVerdictMap(rule)+" }")
	} else {
		parts = append(parts, "counter dnat to "+rule.TargetAddress+":"+strconv.Itoa(rule.TargetPort))
	}
	parts = append(parts, "comment "+nftQuote("routerd "+rule.ResourceKind+" "+rule.Name+commentSuffix))
	buf.WriteString("    " + strings.Join(parts, " ") + "\n")
}

func writeIngressHairpinSNATRules(buf *bytes.Buffer, rules []ingressNATRule) {
	for _, rule := range rules {
		if rule.ListenAddress == "" {
			continue
		}
		for _, ifname := range rule.HairpinInterfaces {
			for _, target := range ingressRuleTargets(rule) {
				var parts []string
				parts = append(parts, "iifname "+nftQuote(ifname))
				parts = append(parts, "ip daddr "+target.Address)
				parts = append(parts, rule.Protocol)
				parts = append(parts, "dport "+strconv.Itoa(target.Port))
				parts = append(parts, "ct original ip daddr "+rule.ListenAddress)
				parts = append(parts, "ct original proto-dst "+strconv.Itoa(rule.ListenPort))
				parts = append(parts, "counter masquerade")
				parts = append(parts, "comment "+nftQuote("routerd "+rule.ResourceKind+" "+rule.Name+" hairpin"))
				buf.WriteString("    " + strings.Join(parts, " ") + "\n")
			}
		}
	}
}

func ingressUsesDistribution(rule ingressNATRule) bool {
	return len(rule.Targets) > 1 && (rule.Selection == "sourceHash" || rule.Selection == "random")
}

func ingressSelectionExpression(rule ingressNATRule) string {
	switch rule.Selection {
	case "random":
		return "numgen random mod " + strconv.Itoa(len(rule.Targets))
	default:
		return "jhash ip saddr mod " + strconv.Itoa(len(rule.Targets))
	}
}

func ingressDNATVerdictMap(rule ingressNATRule) string {
	parts := make([]string, 0, len(rule.Targets))
	for i := range rule.Targets {
		parts = append(parts, strconv.Itoa(i)+" : jump "+ingressDNATChainName(rule, i))
	}
	return strings.Join(parts, ", ")
}

func ingressDNATChainName(rule ingressNATRule, index int) string {
	return "ingress_" + sanitizeNftIdentifier(rule.ResourceKind) + "_" + sanitizeNftIdentifier(rule.Name) + "_" + strconv.Itoa(index)
}

func sanitizeNftIdentifier(value string) string {
	value = strings.TrimSpace(strings.ToLower(value))
	var b strings.Builder
	for _, r := range value {
		switch {
		case r >= 'a' && r <= 'z':
			b.WriteRune(r)
		case r >= '0' && r <= '9':
			b.WriteRune(r)
		default:
			b.WriteByte('_')
		}
	}
	out := strings.Trim(b.String(), "_")
	if out == "" {
		return "resource"
	}
	return out
}

func ingressRuleTargets(rule ingressNATRule) []ingressNATTarget {
	if len(rule.Targets) > 0 {
		return rule.Targets
	}
	return []ingressNATTarget{{Address: rule.TargetAddress, Port: rule.TargetPort}}
}

func writeNAT44RenderRule(buf *bytes.Buffer, rule NAT44RenderRule, addressSets map[string]nftIPAddressSet) error {
	if rule.EgressInterface == "" {
		return fmt.Errorf("nat44 rule %q has empty egress interface", rule.Name)
	}
	matches, err := nftIPv4Matches("nat44 rule "+rule.Name, nftIPv4MatchSpec{
		SourceCIDRs:               rule.SourceRanges,
		DestinationCIDRs:          rule.DestinationCIDRs,
		DestinationSetRefs:        rule.DestinationSetRefs,
		ExcludeDestinationCIDRs:   rule.ExcludeDestinationCIDRs,
		ExcludeDestinationSetRefs: rule.ExcludeDestinationSetRefs,
	}, addressSets)
	if err != nil {
		return err
	}
	for _, match := range matches {
		expr := "masquerade"
		switch rule.Type {
		case "masquerade":
		case "snat":
			if rule.SNATAddress == "" {
				return fmt.Errorf("nat44 rule %q needs snat address", rule.Name)
			}
			expr = "snat to " + rule.SNATAddress
		default:
			return fmt.Errorf("nat44 rule %q has unsupported type %q", rule.Name, rule.Type)
		}
		buf.WriteString("    oifname " + nftQuote(rule.EgressInterface) + " " + match + " " + expr + "\n")
	}
	return nil
}

func writeTCPMSSClampTable(buf *bytes.Buffer, aliases map[string]string, policies []pathMTUPolicy) error {
	writeNftTablePreamble(buf, "inet", "routerd_mss")
	buf.WriteString("table inet routerd_mss {\n")
	buf.WriteString("  chain forward {\n")
	buf.WriteString("    type filter hook forward priority mangle; policy accept;\n")
	for _, policy := range policies {
		fromIfname := aliases[policy.Spec.FromInterface]
		if fromIfname == "" {
			return fmt.Errorf("%s references fromInterface with empty ifname", policy.ResourceID)
		}
		var toIfnames []string
		for _, name := range policy.Spec.ToInterfaces {
			ifname := aliases[name]
			if ifname == "" {
				return fmt.Errorf("%s references toInterface with empty ifname %q", policy.ResourceID, name)
			}
			toIfnames = append(toIfnames, ifname)
		}
		sort.Strings(toIfnames)
		toIfnames = compactStrings(toIfnames)
		match := nftIfnameMatch("iifname", []string{fromIfname}) + " " + nftIfnameMatch("oifname", toIfnames)
		if pathMTUFamilyEnabled(policy.Spec.TCPMSSClamp.Families, "ipv4") {
			mss := policy.MTU - 40
			if mss < 536 {
				return fmt.Errorf("%s computed IPv4 MSS %d is too small", policy.ResourceID, mss)
			}
			mssValue := strconv.Itoa(mss)
			buf.WriteString("    " + match + " ip protocol tcp tcp flags syn / syn,rst tcp option maxseg size > " + mssValue + " tcp option maxseg size set " + mssValue + "\n")
		}
		if pathMTUFamilyEnabled(policy.Spec.TCPMSSClamp.Families, "ipv6") {
			mss := policy.MTU - 60
			if mss < 1220 {
				return fmt.Errorf("%s computed IPv6 MSS %d is too small", policy.ResourceID, mss)
			}
			mssValue := strconv.Itoa(mss)
			buf.WriteString("    " + match + " meta nfproto ipv6 tcp flags syn / syn,rst tcp option maxseg size > " + mssValue + " tcp option maxseg size set " + mssValue + "\n")
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
	return nil
}

func writeIPv4PolicyRouteTable(buf *bytes.Buffer, policies []api.Resource, addressSets map[string]nftIPAddressSet) error {
	writeNftTablePreamble(buf, "ip", "routerd_policy")
	buf.WriteString("table ip routerd_policy {\n")
	writeIPv4AddressSets(buf, referencedIPv4PolicyIPAddressSets(policies, addressSets))
	buf.WriteString("  chain prerouting {\n")
	buf.WriteString("    type filter hook prerouting priority mangle; policy accept;\n")
	for _, res := range policies {
		spec, err := res.EgressRoutePolicySpec()
		if err != nil {
			return err
		}
		matches, err := nftEgressRoutePolicyMatches(res.ID(), spec, addressSets)
		if err != nil {
			return err
		}
		for _, candidate := range spec.Candidates {
			if len(candidate.Targets) > 0 {
				hashExpr, err := nftEgressRoutePolicyHash(res.ID(), spec)
				if err != nil {
					return err
				}
				markMap := nftEgressRoutePolicyTargetMarkMap(candidate.Targets)
				for _, match := range matches {
					buf.WriteString("    " + match + " ct mark != 0x0 meta mark set ct mark\n")
					prefix := strings.TrimSpace(match + " ct mark 0x0")
					buf.WriteString("    " + prefix + " meta mark set " + hashExpr + " mod " + strconv.Itoa(len(candidate.Targets)) + " map { " + markMap + " }\n")
					buf.WriteString("    " + prefix + " ct mark set meta mark\n")
				}
				continue
			}
			if spec.Mode == "priority" {
				continue
			}
			if candidate.Mark == 0 {
				continue
			}
			for _, match := range matches {
				buf.WriteString("    " + match + " meta mark set " + nftMark(candidate.Mark) + "\n")
			}
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
	return nil
}

func nftEgressRoutePolicyMatches(resourceID string, spec api.EgressRoutePolicySpec, addressSets map[string]nftIPAddressSet) ([]string, error) {
	return nftIPv4Matches(resourceID, nftIPv4MatchSpec{
		SourceCIDRs:               spec.SourceCIDRs,
		DestinationCIDRs:          spec.DestinationCIDRs,
		DestinationSetRefs:        spec.DestinationSetRefs,
		ExcludeDestinationCIDRs:   spec.ExcludeDestinationCIDRs,
		ExcludeDestinationSetRefs: spec.ExcludeDestinationSetRefs,
	}, addressSets)
}

func nftIPv4CIDRMatches(resourceID string, sourceCIDRs, destinationCIDRs, excludeDestinationCIDRs []string) ([]string, error) {
	return nftIPv4Matches(resourceID, nftIPv4MatchSpec{
		SourceCIDRs:             sourceCIDRs,
		DestinationCIDRs:        destinationCIDRs,
		ExcludeDestinationCIDRs: excludeDestinationCIDRs,
	}, nil)
}

type nftIPv4MatchSpec struct {
	SourceCIDRs               []string
	DestinationCIDRs          []string
	DestinationSetRefs        []string
	ExcludeDestinationCIDRs   []string
	ExcludeDestinationSetRefs []string
}

func nftIPv4Matches(resourceID string, spec nftIPv4MatchSpec, addressSets map[string]nftIPAddressSet) ([]string, error) {
	var sources []string
	if len(spec.SourceCIDRs) == 0 {
		sources = []string{""}
	} else {
		for _, cidr := range spec.SourceCIDRs {
			prefix, err := netip.ParsePrefix(cidr)
			if err != nil || !prefix.Addr().Is4() {
				return nil, fmt.Errorf("%s has invalid IPv4 source CIDR %q", resourceID, cidr)
			}
			sources = append(sources, "ip saddr "+prefix.Masked().String())
		}
	}
	var destinations []string
	if len(spec.DestinationCIDRs) == 0 && len(spec.DestinationSetRefs) == 0 {
		destinations = []string{""}
	} else {
		for _, cidr := range spec.DestinationCIDRs {
			prefix, err := netip.ParsePrefix(cidr)
			if err != nil || !prefix.Addr().Is4() {
				return nil, fmt.Errorf("%s has invalid IPv4 destination CIDR %q", resourceID, cidr)
			}
			destinations = append(destinations, "ip daddr "+prefix.Masked().String())
		}
		for _, ref := range spec.DestinationSetRefs {
			setName, err := nftIPAddressSetRefName(resourceID, ref, addressSets)
			if err != nil {
				return nil, err
			}
			destinations = append(destinations, "ip daddr @"+setName)
		}
	}
	excludes, err := nftIPv4ExcludedDestinationMatch(resourceID, spec.ExcludeDestinationCIDRs, spec.ExcludeDestinationSetRefs, addressSets)
	if err != nil {
		return nil, err
	}
	var matches []string
	for _, source := range sources {
		for _, destination := range destinations {
			matches = append(matches, strings.Join(strings.Fields(strings.Join([]string{source, destination, excludes}, " ")), " "))
		}
	}
	return matches, nil
}

func nftIPv4ExcludedDestinationMatch(resourceID string, cidrs, setRefs []string, addressSets map[string]nftIPAddressSet) (string, error) {
	var out []string
	for _, cidr := range cidrs {
		prefix, err := netip.ParsePrefix(cidr)
		if err != nil || !prefix.Addr().Is4() {
			return "", fmt.Errorf("%s has invalid IPv4 excluded destination CIDR %q", resourceID, cidr)
		}
		out = append(out, "ip daddr !="+prefix.Masked().String())
	}
	for _, ref := range setRefs {
		setName, err := nftIPAddressSetRefName(resourceID, ref, addressSets)
		if err != nil {
			return "", err
		}
		out = append(out, "ip daddr != @"+setName)
	}
	return strings.Join(out, " "), nil
}

func nftIPAddressSetRefName(resourceID, ref string, sets map[string]nftIPAddressSet) (string, error) {
	set, err := nftIPAddressSetRef(resourceID, ref, sets)
	if err != nil {
		return "", err
	}
	return set.SetName, nil
}

func nftIPAddressSetRef(resourceID, ref string, sets map[string]nftIPAddressSet) (nftIPAddressSet, error) {
	ref = strings.TrimSpace(ref)
	if ref == "" {
		return nftIPAddressSet{}, fmt.Errorf("%s has empty IPAddressSet reference", resourceID)
	}
	if sets == nil {
		return nftIPAddressSet{}, fmt.Errorf("%s cannot resolve IPAddressSet reference %q without router context", resourceID, ref)
	}
	set, ok := sets[ref]
	if !ok {
		return nftIPAddressSet{}, fmt.Errorf("%s references missing IPAddressSet %q", resourceID, ref)
	}
	return set, nil
}

func nftEgressRoutePolicyHash(resourceID string, spec api.EgressRoutePolicySpec) (string, error) {
	var fields []string
	for _, field := range spec.HashFields {
		switch field {
		case "sourceAddress":
			fields = append(fields, "ip saddr")
		case "destinationAddress":
			fields = append(fields, "ip daddr")
		default:
			return "", fmt.Errorf("%s has unsupported hash field %q", resourceID, field)
		}
	}
	if len(fields) == 0 {
		return "", fmt.Errorf("%s requires at least one hash field", resourceID)
	}
	return "jhash " + strings.Join(fields, " . "), nil
}

func nftEgressRoutePolicyTargetMarkMap(targets []api.EgressRoutePolicyTarget) string {
	parts := make([]string, 0, len(targets))
	for i, target := range targets {
		parts = append(parts, strconv.Itoa(i)+" : "+nftMark(target.Mark))
	}
	return strings.Join(parts, ", ")
}

func egressPolicyRendersPolicyMarks(spec api.EgressRoutePolicySpec) bool {
	mode := defaultString(spec.Mode, "")
	if mode == "mark" || mode == "hash" {
		return true
	}
	for _, candidate := range spec.Candidates {
		if len(candidate.Targets) > 0 {
			return true
		}
		if candidate.Mark != 0 {
			return mode != "priority"
		}
	}
	return false
}

func nftSourceNATRules(ifname, cidr string, translation api.IPv4NATTranslationSpec) ([]string, error) {
	match := "oifname " + nftQuote(ifname) + " ip saddr " + cidr + " "
	expr, err := nftNATExpression(translation)
	if err != nil {
		return nil, err
	}
	if defaultString(translation.PortMapping.Type, "auto") == "range" {
		return []string{
			match + "meta l4proto { tcp, udp } " + nftNATExpressionWithPortRange(expr, translation.PortMapping),
			match + expr,
		}, nil
	}
	portSuffix, err := nftPortMapping(translation.PortMapping)
	if err != nil {
		return nil, err
	}
	return []string{match + expr + portSuffix}, nil
}

func nftNATExpressionWithPortRange(expr string, portMapping api.IPv4NATPortMappingSpec) string {
	portRange := strconv.Itoa(portMapping.Start) + "-" + strconv.Itoa(portMapping.End)
	if strings.HasPrefix(expr, "snat to ") {
		return expr + ":" + portRange
	}
	return expr + " to :" + portRange
}

func nftNATExpression(translation api.IPv4NATTranslationSpec) (string, error) {
	switch translation.Type {
	case "interfaceAddress":
		return "masquerade", nil
	case "address":
		if translation.Address == "" {
			return "", fmt.Errorf("translation.address is required")
		}
		return "snat to " + translation.Address, nil
	case "pool":
		if len(translation.Addresses) == 0 {
			return "", fmt.Errorf("translation.addresses is required")
		}
		addresses := append([]string(nil), translation.Addresses...)
		sort.Strings(addresses)
		return "snat to " + strings.Join(addresses, "-"), nil
	default:
		return "", fmt.Errorf("unsupported translation.type %q", translation.Type)
	}
}

func nftPortMapping(portMapping api.IPv4NATPortMappingSpec) (string, error) {
	switch defaultString(portMapping.Type, "auto") {
	case "auto":
		return "", nil
	case "range":
		return "", nil
	case "preserve":
		return " persistent", nil
	default:
		return "", fmt.Errorf("unsupported portMapping.type %q", portMapping.Type)
	}
}

func nftQuote(value string) string {
	return strconv.Quote(value)
}

func nftMark(value int) string {
	return fmt.Sprintf("0x%x", value)
}

func nftIfnameMatch(key string, ifnames []string) string {
	if len(ifnames) == 1 {
		return key + " " + nftQuote(ifnames[0])
	}
	quoted := make([]string, 0, len(ifnames))
	for _, ifname := range ifnames {
		quoted = append(quoted, nftQuote(ifname))
	}
	return key + " { " + strings.Join(quoted, ", ") + " }"
}

func compactStrings(values []string) []string {
	var result []string
	for _, value := range values {
		if len(result) == 0 || result[len(result)-1] != value {
			result = append(result, value)
		}
	}
	return result
}

func stringInSlice(needle string, values []string) bool {
	for _, value := range values {
		if value == needle {
			return true
		}
	}
	return false
}
