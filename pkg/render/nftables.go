func nftIPv4SourcesMatch(resourceID string, sources []string) (string, error) {
	if len(sources) == 0 {
		return "", nil
	}
	var values []string
	for _, source := range sources {
		prefix, err := netip.ParsePrefix(source)
		if err != nil || !prefix.Addr().Is4() {
			return "", fmt.Errorf("%s has invalid IPv4 source CIDR %q", resourceID, source)
		}
		values = append(values, prefix.Masked().String())
	}
	sort.Strings(values)
	if len(values) == 1 {
		return " ip saddr " + values[0], nil
	}
	return " ip saddr { " + strings.Join(values, ", ") + " }", nil
}

func writeIPv4SourceNATTable(buf *bytes.Buffer, aliases map[string]string, nats []api.Resource, nat44Rules []api.Resource) error {
	buf.WriteString("table ip routerd_nat {\n")
	buf.WriteString("  chain postrouting {\n")
	buf.WriteString("    type nat hook postrouting priority srcnat; policy accept;\n")
	for _, res := range nats {
		spec, err := res.IPv4SourceNATSpec()
		if err != nil {
			return err
		}
		ifname := aliases[spec.OutboundInterface]
		if ifname == "" {
			return fmt.Errorf("%s references outbound interface with empty ifname", res.ID())
		}
		for _, cidr := range spec.SourceCIDRs {
			prefix, err := netip.ParsePrefix(cidr)
			if err != nil || !prefix.Addr().Is4() {
				return fmt.Errorf("%s has invalid IPv4 source CIDR %q", res.ID(), cidr)
			}
			rules, err := nftSourceNATRules(ifname, prefix.Masked().String(), spec.Translation)
			if err != nil {
				return fmt.Errorf("%s: %w", res.ID(), err)
			}
			for _, rule := range rules {
				buf.WriteString("    " + rule + "\n")
			}
		}
	}
	for _, res := range nat44Rules {
		spec, err := res.NAT44RuleSpec()
		if err != nil {
			return err
		}
		if spec.EgressInterface == "" {
			return fmt.Errorf("%s needs resolved egressInterface for static nftables render", res.ID())
		}
		ifname := aliases[spec.EgressInterface]
		if ifname == "" {
			return fmt.Errorf("%s references egress interface with empty ifname", res.ID())
		}
		if err := writeNAT44RenderRule(buf, NAT44RenderRule{
			Name:            res.Metadata.Name,
			Type:            spec.Type,
			EgressInterface: ifname,
			SourceRanges:    spec.SourceRanges,
			SNATAddress:     spec.SNATAddress,
		}); err != nil {
			return err
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
	return nil
}

func writeNAT44RenderRule(buf *bytes.Buffer, rule NAT44RenderRule) error {
	if rule.EgressInterface == "" {
		return fmt.Errorf("nat44 rule %q has empty egress interface", rule.Name)
	}
	matches, err := nftIPv4CIDRMatches("nat44 rule "+rule.Name, rule.SourceRanges, rule.DestinationCIDRs, rule.ExcludeDestinationCIDRs)
	if err != nil {
		return err
	}
	for _, match := range matches {
		expr := "masquerade"
		switch rule.Type {
		case "masquerade":
		case "snat":
			if rule.SNATAddress == "" {
				return fmt.Errorf("nat44 rule %q needs snat address", rule.Name)
			}
			expr = "snat to " + rule.SNATAddress
		default:
			return fmt.Errorf("nat44 rule %q has unsupported type %q", rule.Name, rule.Type)
		}
		buf.WriteString("    oifname " + nftQuote(rule.EgressInterface) + " " + match + " " + expr + "\n")
	}
	return nil
}

func writeTCPMSSClampTable(buf *bytes.Buffer, aliases map[string]string, policies []pathMTUPolicy) error {
	buf.WriteString("table inet routerd_mss {\n")
	buf.WriteString("  chain forward {\n")
	buf.WriteString("    type filter hook forward priority mangle; policy accept;\n")
	for _, policy := range policies {
		fromIfname := aliases[policy.Spec.FromInterface]
		if fromIfname == "" {
			return fmt.Errorf("%s references fromInterface with empty ifname", policy.Resource.ID())
		}
		var toIfnames []string
		for _, name := range policy.Spec.ToInterfaces {
			ifname := aliases[name]
			if ifname == "" {
				return fmt.Errorf("%s references toInterface with empty ifname %q", policy.Resource.ID(), name)
			}
			toIfnames = append(toIfnames, ifname)
		}
		sort.Strings(toIfnames)
		toIfnames = compactStrings(toIfnames)
		match := nftIfnameMatch("iifname", []string{fromIfname}) + " " + nftIfnameMatch("oifname", toIfnames)
		if pathMTUFamilyEnabled(policy.Spec.TCPMSSClamp.Families, "ipv4") {
			mss := policy.MTU - 40
			if mss < 536 {
				return fmt.Errorf("%s computed IPv4 MSS %d is too small", policy.Resource.ID(), mss)
			}
			buf.WriteString("    " + match + " ip protocol tcp tcp flags syn / syn,rst tcp option maxseg size set " + strconv.Itoa(mss) + "\n")
		}
		if pathMTUFamilyEnabled(policy.Spec.TCPMSSClamp.Families, "ipv6") {
			mss := policy.MTU - 60
			if mss < 1220 {
				return fmt.Errorf("%s computed IPv6 MSS %d is too small", policy.Resource.ID(), mss)
			}
			buf.WriteString("    " + match + " meta nfproto ipv6 tcp flags syn / syn,rst tcp option maxseg size set " + strconv.Itoa(mss) + "\n")
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
	return nil
}

func writeIPv4PolicyRouteTable(buf *bytes.Buffer, policies []api.Resource, policySets []api.Resource) error {
	buf.WriteString("table ip routerd_policy {\n")
	buf.WriteString("  chain prerouting {\n")
	buf.WriteString("    type filter hook prerouting priority mangle; policy accept;\n")
	for _, res := range policies {
		spec, err := res.IPv4PolicyRouteSpec()
		if err != nil {
			return err
		}
		matches, err := nftIPv4PolicyRouteMatches(res.ID(), spec)
		if err != nil {
			return err
		}
		for _, match := range matches {
			buf.WriteString("    " + match + " meta mark set " + nftMark(spec.Mark) + "\n")
		}
	}
	for _, res := range policySets {
		spec, err := res.IPv4PolicyRouteSetSpec()
		if err != nil {
			return err
		}
		matches, err := nftIPv4PolicyRouteSetMatches(res.ID(), spec)
		if err != nil {
			return err
		}
		hashExpr, err := nftIPv4PolicyRouteSetHash(res.ID(), spec)
		if err != nil {
			return err
		}
		markMap := nftPolicyRouteSetMarkMap(spec.Targets)
		for _, match := range matches {
			buf.WriteString("    " + match + " ct mark != 0x0 meta mark set ct mark\n")
			prefix := strings.TrimSpace(match + " ct mark 0x0")
			buf.WriteString("    " + prefix + " meta mark set " + hashExpr + " mod " + strconv.Itoa(len(spec.Targets)) + " map { " + markMap + " }\n")
			buf.WriteString("    " + prefix + " ct mark set meta mark\n")
		}
	}
	buf.WriteString("  }\n")
	buf.WriteString("}\n")
	return nil
}

func nftIPv4PolicyRouteMatches(resourceID string, spec api.IPv4PolicyRouteSpec) ([]string, error) {
	return nftIPv4CIDRMatches(resourceID, spec.SourceCIDRs, spec.DestinationCIDRs, spec.ExcludeDestinationCIDRs)
}

func nftIPv4PolicyRouteSetMatches(resourceID string, spec api.IPv4PolicyRouteSetSpec) ([]string, error) {
	return nftIPv4CIDRMatches(resourceID, spec.SourceCIDRs, spec.DestinationCIDRs, spec.ExcludeDestinationCIDRs)
}

func nftIPv4CIDRMatches(resourceID string, sourceCIDRs, destinationCIDRs, excludeDestinationCIDRs []string) ([]string, error) {
	var sources []string
	if len(sourceCIDRs) == 0 {
		sources = []string{""}
	} else {
		for _, cidr := range sourceCIDRs {
			prefix, err := netip.ParsePrefix(cidr)
			if err != nil || !prefix.Addr().Is4() {
				return nil, fmt.Errorf("%s has invalid IPv4 source CIDR %q", resourceID, cidr)
			}
			sources = append(sources, "ip saddr "+prefix.Masked().String())
		}
	}
	var destinations []string
	if len(destinationCIDRs) == 0 {
		destinations = []string{""}
	} else {
		for _, cidr := range destinationCIDRs {
			prefix, err := netip.ParsePrefix(cidr)
			if err != nil || !prefix.Addr().Is4() {
				return nil, fmt.Errorf("%s has invalid IPv4 destination CIDR %q", resourceID, cidr)
			}
			destinations = append(destinations, "ip daddr "+prefix.Masked().String())
		}
	}
	excludes, err := nftIPv4ExcludedDestinationMatch(resourceID, excludeDestinationCIDRs)
	if err != nil {
		return nil, err
	}
	var matches []string
	for _, source := range sources {
		for _, destination := range destinations {
			matches = append(matches, strings.Join(strings.Fields(strings.Join([]string{source, destination, excludes}, " ")), " "))
		}
	}
	return matches, nil
}

func nftIPv4ExcludedDestinationMatch(resourceID string, cidrs []string) (string, error) {
	var out []string
	for _, cidr := range cidrs {
		prefix, err := netip.ParsePrefix(cidr)
		if err != nil || !prefix.Addr().Is4() {
			return "", fmt.Errorf("%s has invalid IPv4 excluded destination CIDR %q", resourceID, cidr)
		}
		out = append(out, "ip daddr !="+prefix.Masked().String())
	}
	return strings.Join(out, " "), nil
}

func nftIPv4PolicyRouteSetHash(resourceID string, spec api.IPv4PolicyRouteSetSpec) (string, error) {
	var fields []string
	for _, field := range spec.HashFields {
		switch field {
		case "sourceAddress":
			fields = append(fields, "ip saddr")
		case "destinationAddress":
			fields = append(fields, "ip daddr")
		default:
			return "", fmt.Errorf("%s has unsupported hash field %q", resourceID, field)
		}
	}
	if len(fields) == 0 {
		return "", fmt.Errorf("%s requires at least one hash field", resourceID)
	}
	return "jhash " + strings.Join(fields, " . "), nil
}

func nftPolicyRouteSetMarkMap(targets []api.IPv4PolicyRouteTarget) string {
	parts := make([]string, 0, len(targets))
	for i, target := range targets {
		parts = append(parts, strconv.Itoa(i)+" : "+nftMark(target.Mark))
	}
	return strings.Join(parts, ", ")
}

func nftSourceNATRules(ifname, cidr string, translation api.IPv4NATTranslationSpec) ([]string, error) {
	match := "oifname " + nftQuote(ifname) + " ip saddr " + cidr + " "
	expr, err := nftNATExpression(translation)
	if err != nil {
		return nil, err
	}
	if defaultString(translation.PortMapping.Type, "auto") == "range" {
		return []string{
			match + "meta l4proto { tcp, udp } " + nftNATExpressionWithPortRange(expr, translation.PortMapping),
			match + expr,
		}, nil
	}
	portSuffix, err := nftPortMapping(translation.PortMapping)
	if err != nil {
		return nil, err
	}
	return []string{match + expr + portSuffix}, nil
}

func nftNATExpressionWithPortRange(expr string, portMapping api.IPv4NATPortMappingSpec) string {
	portRange := strconv.Itoa(portMapping.Start) + "-" + strconv.Itoa(portMapping.End)
	if strings.HasPrefix(expr, "snat to ") {
		return expr + ":" + portRange
	}
	return expr + " to :" + portRange
}

func nftNATExpression(translation api.IPv4NATTranslationSpec) (string, error) {
	switch translation.Type {
	case "interfaceAddress":
		return "masquerade", nil
	case "address":
		if translation.Address == "" {
			return "", fmt.Errorf("translation.address is required")
		}
		return "snat to " + translation.Address, nil
	case "pool":
		if len(translation.Addresses) == 0 {
			return "", fmt.Errorf("translation.addresses is required")
		}
		addresses := append([]string(nil), translation.Addresses...)
		sort.Strings(addresses)
		return "snat to " + strings.Join(addresses, "-"), nil
	default:
		return "", fmt.Errorf("unsupported translation.type %q", translation.Type)
	}
}

func nftPortMapping(portMapping api.IPv4NATPortMappingSpec) (string, error) {
	switch defaultString(portMapping.Type, "auto") {
	case "auto":
		return "", nil
	case "range":
		return "", nil
	case "preserve":
		return " persistent", nil
	default:
		return "", fmt.Errorf("unsupported portMapping.type %q", portMapping.Type)
	}
}

func nftQuote(value string) string {
	return strconv.Quote(value)
}

func nftMark(value int) string {
	return fmt.Sprintf("0x%x", value)
}

func nftIfnameMatch(key string, ifnames []string) string {
	if len(ifnames) == 1 {
		return key + " " + nftQuote(ifnames[0])
	}
	quoted := make([]string, 0, len(ifnames))
	for _, ifname := range ifnames {
		quoted = append(quoted, nftQuote(ifname))
	}
	return key + " { " + strings.Join(quoted, ", ") + " }"
}

func compactStrings(values []string) []string {
	var result []string
	for _, value := range values {
		if len(result) == 0 || result[len(result)-1] != value {
			result = append(result, value)
		}
	}
	return result
}
