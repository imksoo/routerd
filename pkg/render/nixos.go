func nixOSSystemdUnits(router *api.Router) ([]nixOSSystemdUnit, error) {
	var out []nixOSSystemdUnit
	explicit := map[string]bool{}
	host, err := nixOSHost(router)
	if err != nil {
		return nil, err
	}
	routerdSupervisesClients := nixOSRouterdSupervisesClientDaemons(host.RouterdService)
	telemetryEnv, err := TelemetryEnvironment(router)
	if err != nil {
		return nil, err
	}
	dpiSocket := ""
	if RouterWantsDPIClassifier(router) {
		dpiSocket = "/run/routerd/dpi-classifier/default.sock"
	}
	wantsNDPIAgent := RouterWantsNDPIAgent(router)
	if RouterWantsDPIClassifier(router) {
		out = append(out, nixOSSystemdUnit{Name: DPIClassifierUnitName, Spec: DPIClassifierSystemdSpec("/run")})
	}
	if wantsNDPIAgent && !explicit[NDPIAgentUnitName] {
		out = append(out, nixOSSystemdUnit{Name: NDPIAgentUnitName, Spec: NDPIAgentSystemdSpec("/run")})
	}
	aliases := linkAliases(router)
	for _, res := range router.Spec.Resources {
		if res.Kind != "TailscaleNode" {
			continue
		}
		spec, err := res.TailscaleNodeSpec()
		if err != nil {
			return nil, err
		}
		if firstNonEmpty(spec.State, "present") != "absent" {
			spec.BinaryPath = firstNonEmpty(spec.BinaryPath, "tailscale")
		}
		unit := renderTailscaleNixOSUnit(res.Metadata.Name, spec)
		if explicit[unit.Name] {
			continue
		}
		out = append(out, unit)
	}
	for _, res := range router.Spec.Resources {
		if res.Kind != "HealthCheck" {
			continue
		}
		spec, err := res.HealthCheckSpec()
		if err != nil {
			return nil, err
		}
		name := "routerd-healthcheck@" + res.Metadata.Name + ".service"
		if explicit[name] {
			continue
		}
		out = append(out, nixOSSystemdUnit{
			Name: name,
			Spec: HealthCheckDaemonSystemdSpec(HealthCheckDaemonUnitOptions{
				Resource:    res.Metadata.Name,
				Spec:        spec,
				Router:      router,
				Aliases:     aliases,
				Environment: telemetryEnv,
				RuntimeRoot: "/run",
				StateRoot:   "/var/lib",
				LogRoot:     "/var/log",
			}),
		})
	}
	for _, res := range router.Spec.Resources {
		if res.Kind != "DNSResolver" {
			continue
		}
		spec, err := res.DNSResolverSpec()
		if err != nil {
			return nil, err
		}
		name := "routerd-dns-resolver@" + res.Metadata.Name + ".service"
		if explicit[name] {
			continue
		}
		out = append(out, nixOSSystemdUnit{
			Name: name,
			Spec: DNSResolverSystemdSpec(res.Metadata.Name, spec, "/usr/local/sbin/routerd-dns-resolver", ""),
		})
	}
	for _, res := range router.Spec.Resources {
		if res.Kind != "DHCPv4Client" {
			continue
		}
		if routerdSupervisesClients {
			continue
		}
		spec, err := res.DHCPv4ClientSpec()
		if err != nil {
			return nil, err
		}
		ifname := aliases[spec.Interface]
		if ifname == "" {
			ifname = spec.Interface
		}
		if ifname == "" {
			return nil, fmt.Errorf("%s needs spec.interface", res.ID())
		}
		name := "routerd-dhcpv4-client@" + res.Metadata.Name + ".service"
		if explicit[name] {
			continue
		}
		out = append(out, nixOSSystemdUnit{
			Name: name,
			Spec: dhcpv4ClientSystemdSpec(res.Metadata.Name, ifname, spec, telemetryEnv),
		})
	}
	for _, res := range router.Spec.Resources {
		if res.Kind != "PPPoESession" {
			continue
		}
		spec, err := res.PPPoESessionSpec()
		if err != nil {
			return nil, err
		}
		ifname := aliases[spec.Interface]
		if ifname == "" {
			ifname = spec.Interface
		}
		if ifname == "" {
			return nil, fmt.Errorf("%s needs spec.interface", res.ID())
		}
		name := "routerd-pppoe-client@" + res.Metadata.Name + ".service"
		if explicit[name] {
			continue
		}
		unit, err := pppoeSessionSystemdSpec(res.Metadata.Name, ifname, spec, telemetryEnv)
		if err != nil {
			return nil, fmt.Errorf("%s: %w", res.ID(), err)
		}
		out = append(out, nixOSSystemdUnit{Name: name, Spec: unit})
	}
	for _, res := range router.Spec.Resources {
		if res.Kind != "FirewallEventLog" {
			continue
		}
		spec, err := res.FirewallEventLogSpec()
		if err != nil {
			return nil, err
		}
		if !spec.Enabled {
			continue
		}
		name := "routerd-firewall-logger.service"
		if explicit[name] {
			continue
		}
		out = append(out, nixOSSystemdUnit{
			Name: name,
			Spec: FirewallLoggerSystemdSpec(spec, dpiSocket),
		})
	}
	if nixOSNeedsDnsmasq(router) {
		name := "routerd-dnsmasq.service"
		if !explicit[name] {
			out = append(out, nixOSSystemdUnit{
				Name: name,
				Spec: dnsmasqNixOSSystemdSpec(),
				Path: []string{"dnsmasq"},
			})
		}
	}
	sort.Slice(out, func(i, j int) bool { return out[i].Name < out[j].Name })
	return out, nil
}

