func nixOSPackages(router *api.Router, host api.NixOSHostSpec) ([]string, []string, error) {
	service := map[string]bool{
		"conntrack-tools": true,
		"dnsmasq":         true,
		"dnsutils":        true,
		"iproute2":        true,
		"iputils":         true,
		"kmod":            true,
		"nettools":        true,
		"nftables":        true,
		"ppp":             true,
		"procps":          true,
		"strongswan":      true,
		"systemd":         true,
		"tcpdump":         true,
		"traceroute":      true,
		"wireguard-tools": true,
	}
	debug := map[string]bool{
		"jq": true,
	}
	hasDeclarativePackages := false
	for _, res := range router.Spec.Resources {
		if res.Kind == "Package" {
			hasDeclarativePackages = true
			spec, err := res.PackageSpec()
			if err != nil {
				return nil, nil, err
			}
			for _, set := range spec.Packages {
				if set.OS != "nixos" {
					continue
				}
				for _, pkg := range set.Names {
					if err := validateNixPackageIdent(pkg); err != nil {
						return nil, nil, err
					}
					debug[pkg] = true
				}
			}
		}
	}
	for _, res := range hostdeps.PackageResources(router) {
		spec, err := res.PackageSpec()
		if err != nil {
			return nil, nil, err
		}
		for _, set := range spec.Packages {
			if set.OS != "nixos" {
				continue
			}
			for _, pkg := range set.Names {
				if err := validateNixPackageIdent(pkg); err != nil {
					return nil, nil, err
				}
				service[pkg] = true
			}
		}
	}
	for _, res := range router.Spec.Resources {
		switch res.Kind {
		case "Bridge":
			spec, err := res.BridgeSpec()
			if err != nil {
				return nil, nil, err
			}
			if api.BoolDefault(spec.RSTP, true) {
				service["mstpd"] = true
				debug["mstpd"] = true
			}
		case "VXLANSegment":
			service["nftables"] = true
			debug["nftables"] = true
		case "DHCPv4Server", "DHCPv6Server":
			service["dnsmasq"] = true
			debug["dnsmasq"] = true
		case "EgressRoutePolicy", "DSLiteTunnel", "WireGuardInterface", "FirewallZone", "FirewallPolicy", "FirewallRule", "ClientPolicy":
			service["nftables"] = true
			debug["nftables"] = true
		case "PPPoESession":
			service["nftables"] = true
			debug["nftables"] = true
			spec, err := res.PPPoESessionSpec()
			if err != nil {
				return nil, nil, err
			}
			if api.BoolDefault(spec.Enabled, true) {
				service["ppp"] = true
				debug["ppp"] = true
			}
		case "TailscaleNode":
			spec, err := res.TailscaleNodeSpec()
			if err != nil {
				return nil, nil, err
			}
			if firstNonEmpty(spec.State, "present") != "absent" {
				service["tailscale"] = true
				debug["tailscale"] = true
			}
		}
	}
	for _, pkg := range host.AdditionalServicePath {
		if err := validateNixPackageIdent(pkg); err != nil {
			return nil, nil, err
		}
		service[pkg] = true
	}
	for _, pkg := range host.AdditionalPackages {
		if err := validateNixPackageIdent(pkg); err != nil {
			return nil, nil, err
		}
		debug[pkg] = true
	}
	if host.DebugSystemPackages {
		for pkg := range service {
			if pkg != "systemd" {
				debug[pkg] = true
			}
		}
		debug["conntrack-tools"] = true
	}
	serviceList := sortedMapKeys(service)
	var debugList []string
	if host.DebugSystemPackages || len(host.AdditionalPackages) > 0 || hasDeclarativePackages {
		debugList = sortedMapKeys(debug)
	}
	return debugList, serviceList, nil
}

func splitHostname(value string) (string, string) {
	host, domain, ok := strings.Cut(value, ".")
	if !ok {
		return value, ""
	}
	return host, domain
}

func sortedMapKeys(values map[string]bool) []string {
	out := make([]string, 0, len(values))
	for value := range values {
		out = append(out, value)
	}
	sort.Strings(out)
	return out
}

func sortedMapKeysString(values map[string]string) []string {
	out := make([]string, 0, len(values))
	for value := range values {
		out = append(out, value)
	}
	sort.Strings(out)
	return out
}

func nixBool(value bool) string {
	if value {
		return "true"
	}
	return "false"
}

func nixString(value string) string {
	return strconv.Quote(value)
}

func shellSingleQuote(value string) string {
	if value == "" {
		return "''"
	}
	return "'" + strings.ReplaceAll(value, "'", "'\"'\"'") + "'"
}

func nixSysctlValue(value string) string {
	if _, err := strconv.Atoi(value); err == nil {
		return value
	}
	switch value {
	case "true", "false":
		return value
	default:
		return nixString(value)
	}
}

func nixStringList(values []string) string {
	var b strings.Builder
	b.WriteString("[")
	for _, value := range values {
		b.WriteString(" ")
		b.WriteString(nixString(value))
	}
	b.WriteString(" ]")
	return b.String()
}

func nixIntListSpaced(values []int) string {
	var b strings.Builder
	for i, value := range values {
		if i > 0 {
			b.WriteString(" ")
		}
		b.WriteString(strconv.Itoa(value))
	}
	return b.String()
}

func nixOSFirewallAllowedUDPPorts(vxlans []nixOSVXLAN) []int {
	seen := map[int]bool{}
	var ports []int
	for _, v := range vxlans {
		if v.UDPPort == 0 || seen[v.UDPPort] {
			continue
		}
		seen[v.UDPPort] = true
		ports = append(ports, v.UDPPort)
	}
	sort.Ints(ports)
	return ports
}

func nixOSFirewallTrustedInterfaces(vxlans []nixOSVXLAN) []string {
	seen := map[string]bool{}
	var names []string
	for _, v := range vxlans {
		if v.Bridge == "" || seen[v.Bridge] {
			continue
		}
		seen[v.Bridge] = true
		names = append(names, v.Bridge)
	}
	sort.Strings(names)
	return names
}

func validateNixPackageIdent(value string) error {
	if value == "" || strings.ContainsAny(value, " \t\n\r;{}[]()\"'") {
		return fmt.Errorf("invalid Nix package identifier %q", value)
	}
	return nil
}
