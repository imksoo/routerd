package render

import (
	"bytes"
	"fmt"
	"net/netip"
	"sort"
	"strings"

	"routerd/pkg/api"
)

type FreeBSDConfig struct {
	RCConf     []byte
	DHCP6C     []byte
	DHCPClient []byte
}

func FreeBSD(router *api.Router) (FreeBSDConfig, error) {
	aliases := map[string]string{}
	managed := map[string]bool{}
	for _, res := range router.Spec.Resources {
		if res.Kind != "Interface" {
			continue
		}
		spec, err := res.InterfaceSpec()
		if err != nil {
			return FreeBSDConfig{}, err
		}
		aliases[res.Metadata.Name] = spec.IfName
		managed[res.Metadata.Name] = spec.Managed && spec.Owner != "external"
	}

	var rc bytes.Buffer
	rc.WriteString("# Generated by routerd. Do not edit by hand.\n")
	rc.WriteString("gateway_enable=\"YES\"\n")
	rc.WriteString("ipv6_gateway_enable=\"YES\"\n")

	dhcp6cIfaces := map[string]bool{}
	dhclientOptions := map[string]api.IPv4DHCPAddressSpec{}
	var pds []freeBSDPD
	for _, res := range router.Spec.Resources {
		switch res.Kind {
		case "IPv4DHCPAddress":
			spec, err := res.IPv4DHCPAddressSpec()
			if err != nil {
				return FreeBSDConfig{}, err
			}
			if managed[spec.Interface] {
				rc.WriteString(fmt.Sprintf("ifconfig_%s=\"DHCP\"\n", aliases[spec.Interface]))
				if spec.UseRoutes != nil || spec.UseDNS != nil || spec.RouteMetric != 0 {
					dhclientOptions[aliases[spec.Interface]] = spec
				}
			}
		case "IPv6DHCPAddress":
			spec, err := res.IPv6DHCPAddressSpec()
			if err != nil {
				return FreeBSDConfig{}, err
			}
			if managed[spec.Interface] {
				rc.WriteString(fmt.Sprintf("ifconfig_%s_ipv6=\"inet6 accept_rtadv\"\n", aliases[spec.Interface]))
				rc.WriteString(fmt.Sprintf("rtsold_flags=\"%s\"\n", aliases[spec.Interface]))
				rc.WriteString("rtsold_enable=\"YES\"\n")
			}
		case "IPv4StaticAddress":
			spec, err := res.IPv4StaticAddressSpec()
			if err != nil {
				return FreeBSDConfig{}, err
			}
			if managed[spec.Interface] {
				address, err := freeBSDIPv4CIDR(spec.Address)
				if err != nil {
					return FreeBSDConfig{}, fmt.Errorf("%s: %w", res.ID(), err)
				}
				rc.WriteString(fmt.Sprintf("ifconfig_%s=\"inet %s\"\n", aliases[spec.Interface], address))
			}
		case "IPv6PrefixDelegation":
			spec, err := res.IPv6PrefixDelegationSpec()
			if err != nil {
				return FreeBSDConfig{}, err
			}
			ifname := aliases[spec.Interface]
			if ifname == "" {
				return FreeBSDConfig{}, fmt.Errorf("%s references interface with empty ifname", res.ID())
			}
			dhcp6cIfaces[ifname] = true
			pds = append(pds, freeBSDPD{Name: res.Metadata.Name, IfName: ifname, PrefixLength: effectiveIPv6PDPrefixLength(defaultString(spec.Profile, "default"), spec.PrefixLength), IAID: spec.IAID})
		}
	}

	if len(dhcp6cIfaces) > 0 {
		rc.WriteString("dhcp6c_enable=\"YES\"\n")
		rc.WriteString("dhcp6c_interfaces=\"" + strings.Join(sortedFreeBSDMapKeys(dhcp6cIfaces), " ") + "\"\n")
	}

	dhcp6c, err := freeBSDDHCP6C(router, aliases, pds)
	if err != nil {
		return FreeBSDConfig{}, err
	}
	dhclient := freeBSDDHClient(dhclientOptions)
	return FreeBSDConfig{RCConf: rc.Bytes(), DHCP6C: dhcp6c, DHCPClient: dhclient}, nil
}

type freeBSDPD struct {
	Name         string
	IfName       string
	PrefixLength int
	IAID         string
}

func freeBSDDHCP6C(router *api.Router, aliases map[string]string, pds []freeBSDPD) ([]byte, error) {
	if len(pds) == 0 {
		return nil, nil
	}
	sort.Slice(pds, func(i, j int) bool { return pds[i].Name < pds[j].Name })
	var buf bytes.Buffer
	buf.WriteString("# Generated by routerd. Do not edit by hand.\n")
	for i, pd := range pds {
		iaid := i
		if parsed, ok := parseIAID(pd.IAID); ok {
			iaid = int(parsed)
		}
		buf.WriteString(fmt.Sprintf("interface %s {\n", pd.IfName))
		buf.WriteString(fmt.Sprintf("  send ia-pd %d;\n", iaid))
		buf.WriteString("  request domain-name-servers;\n")
		buf.WriteString("};\n\n")
		buf.WriteString(fmt.Sprintf("id-assoc pd %d {\n", iaid))
		for _, res := range router.Spec.Resources {
			if res.Kind != "IPv6DelegatedAddress" {
				continue
			}
			spec, err := res.IPv6DelegatedAddressSpec()
			if err != nil {
				return nil, err
			}
			if spec.PrefixDelegation != pd.Name {
				continue
			}
			ifname := aliases[spec.Interface]
			if ifname == "" {
				return nil, fmt.Errorf("%s references interface with empty ifname", res.ID())
			}
			buf.WriteString(fmt.Sprintf("  prefix-interface %s {\n", ifname))
			buf.WriteString(fmt.Sprintf("    sla-id %s;\n", defaultString(spec.SubnetID, "0")))
			if pd.PrefixLength > 0 && pd.PrefixLength <= 64 {
				buf.WriteString(fmt.Sprintf("    sla-len %d;\n", 64-pd.PrefixLength))
			}
			buf.WriteString("  };\n")
		}
		buf.WriteString("};\n\n")
	}
	return buf.Bytes(), nil
}

func freeBSDDHClient(options map[string]api.IPv4DHCPAddressSpec) []byte {
	if len(options) == 0 {
		return nil
	}
	var buf bytes.Buffer
	buf.WriteString("# Generated by routerd. Do not edit by hand.\n")
	for _, ifname := range sortedFreeBSDMapKeysSpec(options) {
		spec := options[ifname]
		buf.WriteString(fmt.Sprintf("interface \"%s\" {\n", ifname))
		var ignored []string
		if spec.UseRoutes != nil && !*spec.UseRoutes {
			ignored = append(ignored, "routers")
		}
		if spec.UseDNS != nil && !*spec.UseDNS {
			ignored = append(ignored, "domain-name", "domain-name-servers", "domain-search")
		}
		if len(ignored) > 0 {
			buf.WriteString("  ignore " + strings.Join(ignored, ", ") + ";\n")
		}
		buf.WriteString("}\n\n")
	}
	return buf.Bytes()
}

func freeBSDIPv4CIDR(value string) (string, error) {
	prefix, err := netip.ParsePrefix(value)
	if err != nil {
		return "", err
	}
	return prefix.Addr().String() + "/" + fmt.Sprintf("%d", prefix.Bits()), nil
}

func sortedFreeBSDMapKeys(values map[string]bool) []string {
	var keys []string
	for key := range values {
		keys = append(keys, key)
	}
	sort.Strings(keys)
	return keys
}

func sortedFreeBSDMapKeysSpec(values map[string]api.IPv4DHCPAddressSpec) []string {
	var keys []string
	for key := range values {
		keys = append(keys, key)
	}
	sort.Strings(keys)
	return keys
}
